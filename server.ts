  app.post("/api/analyze", (req, res) => {
    const { claim } = req.body;
    if (!claim) {
      return res.status(400).json({ error: "No claim provided" });
    }

    const row = db.prepare("SELECT reply FROM rumors WHERE news_text LIKE ?").get(`%${claim}%`) as { reply: string } | undefined;

    if (row) {
      return res.json({
        found: true,
        verdict: "found",
        verdict_ar: "معلومة مخزنة",
        explanation_ar: row.reply,
        confidence: 100
      });
    } else {
      // Not found, client will handle Gemini call
      return res.json({ found: false });
    }
  });

